/* ARITMÉTICOS */
console.log("======= Aritméticos =======")
let soma = 2 + 2
console.log("Soma: " + soma)
let sub = 7 - 2 
console.log("Sub: " + sub)
let mult = 3 * 3
console.log("Multiplicação: " + mult)
let div = 6 / 2
console.log("Divisão: " + div)
let rest = 10 % 3 // Resto da divisão
console.log("Resto : " + rest)

/* AGRUPAMENTO */
console.log("======= Agrupamento =======")
soma = (3 * 3) + 5
console.log("Soma: " + soma)

/* ATRIBUIÇÃO */
console.log("======= Atribuição =======")
soma = 10
console.log("Soma: " + soma)
soma += 5
console.log("Soma: " + soma)
sub -= 3
console.log("Sub: " + sub)
mult *= 2
console.log("Multiplicação: " + mult)
div /= 9
console.log("Divisão: " + div)
rest %= 2
console.log("Resto : " + rest)

/* INCREMENTAR E DECREMENTAR */
console.log("======= Incremento =======")
soma++
console.log("Soma: " + soma)
console.log("======= Decremento =======")
sub--
console.log("Sub: " + sub)

